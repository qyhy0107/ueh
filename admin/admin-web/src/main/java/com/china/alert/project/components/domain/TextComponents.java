package com.china.alert.project.components.domain;

import java.util.Date;

public class TextComponents {

    /**
     * 组件id
     */
    private String id;

    private String  componentsId;
    /**
     * 组件类型
     */
    private String componentsType;

    /**
     * 页面组件名称
     */
    private String componentsName;

    /**
     * 刷新时间
     */
    private String refreshTime;
    /**
     * 主标题颜色
     */
    private String color;
    /**
     * 主标题字体大小
     */
    private String fontSize;
    /**
     * 字体图标颜色
     */
    private String iconColor;
    /**
     * 字体图标大小
     */
    private String iconSize;
    /**
     * 副标题字体大小
     */
    private String contentSize;
    /**
     * 副标题颜色
     */
    private String contentColor;
    /**
     * 字体图标主标题对齐方式
     */
    private String textAlign;
    /**
     * 副标题对齐方式
     */
    private String contentAlign;
    /**
     * 字体图标
     */
    private String icon;
    /**
     * 是否显示字体图标
     */
    private String isIcon;
    /**
     * 标题是否添加链接
     */
    private String isTitleLink;
    /**
     * 内容是否添链接
     */
    private String isContentLink;
    /**
     * 标题链接网址
     */
    private String titleLink;
    /**
     * 内容链接网址
     */
    private String contentLink;
    /**
     * 数据集
     */
    private String textDataSet;
    /**
     * 主标题是否加粗
     */
    private String mainCaption;
    /**
     * 副标题是否加粗
     */
    private String assistantCaption;

    /**
     * 创建时间
     */
    private Date create_time;

    /**
     * 更新时间
     */
    private Date update_time;
    /**
     * 备注
     */
    private String remark;

    public TextComponents() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getComponentsId() {
        return componentsId;
    }

    public void setComponentsId(String componentsId) {
        this.componentsId = componentsId;
    }

    public String getComponentsType() {
        return componentsType;
    }

    public void setComponentsType(String componentsType) {
        this.componentsType = componentsType;
    }

    public String getRefreshTime() {
        return refreshTime;
    }

    public void setRefreshTime(String refreshTime) {
        this.refreshTime = refreshTime;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getFontSize() {
        return fontSize;
    }

    public void setFontSize(String fontSize) {
        this.fontSize = fontSize;
    }

    public String getIconColor() {
        return iconColor;
    }

    public void setIconColor(String iconColor) {
        this.iconColor = iconColor;
    }

    public String getIconSize() {
        return iconSize;
    }

    public void setIconSize(String iconSize) {
        this.iconSize = iconSize;
    }

    public String getContentSize() {
        return contentSize;
    }

    public void setContentSize(String contentSize) {
        this.contentSize = contentSize;
    }

    public String getContentColor() {
        return contentColor;
    }

    public void setContentColor(String contentColor) {
        this.contentColor = contentColor;
    }

    public String getTextAlign() {
        return textAlign;
    }

    public void setTextAlign(String textAlign) {
        this.textAlign = textAlign;
    }

    public String getContentAlign() {
        return contentAlign;
    }

    public void setContentAlign(String contentAlign) {
        this.contentAlign = contentAlign;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getIsIcon() {
        return isIcon;
    }

    public void setIsIcon(String isIcon) {
        this.isIcon = isIcon;
    }

    public String getIsTitleLink() {
        return isTitleLink;
    }

    public void setIsTitleLink(String isTitleLink) {
        this.isTitleLink = isTitleLink;
    }

    public String getIsContentLink() {
        return isContentLink;
    }

    public void setIsContentLink(String isContentLink) {
        this.isContentLink = isContentLink;
    }

    public String getTitleLink() {
        return titleLink;
    }

    public void setTitleLink(String titleLink) {
        this.titleLink = titleLink;
    }

    public String getContentLink() {
        return contentLink;
    }

    public void setContentLink(String contentLink) {
        this.contentLink = contentLink;
    }

    public String getTextDataSet() {
        return textDataSet;
    }

    public void setTextDataSet(String textDataSet) {
        this.textDataSet = textDataSet;
    }

    public String getComponentsName() {
        return componentsName;
    }

    public void setComponentsName(String componentsName) {
        this.componentsName = componentsName;
    }

    public String getMainCaption() {
        return mainCaption;
    }

    public void setMainCaption(String mainCaption) {
        this.mainCaption = mainCaption;
    }

    public String getAssistantCaption() {
        return assistantCaption;
    }

    public void setAssistantCaption(String assistantCaption) {
        this.assistantCaption = assistantCaption;
    }

    public Date getCreate_time() {
        return create_time;
    }

    public void setCreate_time(Date create_time) {
        this.create_time = create_time;
    }

    public Date getUpdate_time() {
        return update_time;
    }

    public void setUpdate_time(Date update_time) {
        this.update_time = update_time;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
